def load_data(data_dir: str, days: List[int]) -> Tuple[pd.DataFrame, pd.DataFrame]:
    """Load and concatenate all price and trade CSVs."""
    price_frames, trade_frames = [], []
    for day in days:
        p = Path(data_dir) / f"prices_round_2_day_{day}.csv"
        t = Path(data_dir) / f"trades_round_2_day_{day}.csv"
        if not p.exists():
            raise FileNotFoundError(f"Missing: {p}")
        if not t.exists():
            raise FileNotFoundError(f"Missing: {t}")
        pdf = pd.read_csv(p, sep=";")
        tdf = pd.read_csv(t, sep=";")
        tdf["day"] = day
        price_frames.append(pdf)
        trade_frames.append(tdf)

    prices = pd.concat(price_frames, ignore_index=True)
    trades = pd.concat(trade_frames, ignore_index=True)
    prices.sort_values(["day", "timestamp", "product"], inplace=True)
    trades.sort_values(["day", "timestamp"], inplace=True)
    return prices, trades


def build_order_depth(row: pd.Series) -> OrderDepth:
    """Build an OrderDepth from a single price-CSV row."""
    od = OrderDepth()
    for pc, vc in PRICE_COLS["bids"]:
        p, v = row.get(pc), row.get(vc)
        if pd.notna(p) and pd.notna(v) and v > 0:
            od.buy_orders[int(p)] = int(v)
    for pc, vc in PRICE_COLS["asks"]:
        p, v = row.get(pc), row.get(vc)
        if pd.notna(p) and pd.notna(v) and v > 0:
            od.sell_orders[int(p)] = -int(v)  # sell_orders hold NEGATIVE qty
    return od


