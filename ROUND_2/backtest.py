def compare_with_log(tick_results: List[TickResult], log_data: dict) -> dict:
    """
    Compare backtest PnL curve against the submitted log's PnL curve.
    Returns comparison statistics.
    """
    acts = log_data.get("activities", pd.DataFrame())
    if acts.empty:
        return {"error": "No activitiesLog in log file"}

    # Build log PnL series: last pnl per (day, timestamp) across products
    log_pnl: Dict[Tuple, float] = {}
    for _, row in acts.iterrows():
        key = (int(row["day"]), int(row["timestamp"]))
        log_pnl[key] = log_pnl.get(key, 0) + row.get("profit_and_loss", 0)

    # Build backtest series
    bt_pnl = {(r.day, r.timestamp): r.total_pnl for r in tick_results}

    common_keys = sorted(set(log_pnl) & set(bt_pnl))
    if not common_keys:
        return {"error": "No overlapping timestamps between log and backtest"}

    log_vals = [log_pnl[k] for k in common_keys]
    bt_vals  = [bt_pnl[k]  for k in common_keys]
    diffs    = [b - l for b, l in zip(bt_vals, log_vals)]

    # Log final PnL
    log_submission_trades = log_data.get("trades_by_ts", {})
    log_trade_count = sum(len(v) for v in log_submission_trades.values())
    log_final_pnl = log_vals[-1] if log_vals else 0
    bt_final_pnl  = bt_vals[-1]  if bt_vals  else 0

    return dict(
        common_ticks=len(common_keys),
        log_final_pnl=log_final_pnl,
        bt_final_pnl=bt_final_pnl,
        pnl_diff=bt_final_pnl - log_final_pnl,
        mean_abs_diff=float(np.mean(np.abs(diffs))),
        max_abs_diff=float(np.max(np.abs(diffs))),
        log_trade_count=log_trade_count,
        diffs_series=list(zip([str(k) for k in common_keys], diffs)),
    )


# ─────────────────────────────────────────────────────────────────────────────
#  MANUAL CHALLENGE OPTIMISER  (Invest & Expand)
# ─────────────────────────────────────────────────────────────────────────────

def optimise_investment(
    budget: float = 50_000,
    num_players: int = 50,
    our_rank_assumption: int = 25,   # assumed rank (1=fastest, N=slowest)
) -> dict:
    """
    Maximise: research(r%) * scale(s%) * speed_mult(rank) - budget_used
    where r + s + speed_pct <= 100, all in [0, 100].

    Speed multiplier is rank-based. We model it as a continuous function
    by assuming our rank proportional to our investment.
    """
    results = []
    steps = 200
    for r in range(0, steps + 1):
        for s in range(0, steps - r + 1):
            sp = steps - r - s
            r_pct  = r  / steps * 100
            s_pct  = s  / steps * 100
            sp_pct = sp / steps * 100

            research = 200_000 * np.log(1 + r_pct) / np.log(1 + 100)
            scale    = 7 * s_pct / 100

            # Speed rank assumption: linear interpolation
            # If we invest sp_pct, and opponents are uniform [0,100],
            # our rank ≈ fraction of players below us = sp_pct/100
            speed_rank_frac = sp_pct / 100   # 0=lowest, 1=highest
            speed_mult = 0.1 + 0.8 * speed_rank_frac  # 0.1 → 0.9

            gross   = research * scale * speed_mult
            budget_used = (r_pct + s_pct + sp_pct) / 100 * budget
            net     = gross - budget_used
            results.append((net, r_pct, s_pct, sp_pct, gross, budget_used, speed_mult))

    results.sort(reverse=True)
    best = results[0]
    return dict(
        net_pnl=best[0],
        research_pct=best[1],
        scale_pct=best[2],
        speed_pct=best[3],
        gross_pnl=best[4],
        budget_used=best[5],
        speed_mult=best[6],
        top_10=results[:10],
    )


