#  ORDER MATCHING ENGINE
# ─────────────────────────────────────────────────────────────────────────────

class MatchingEngine:
    """
    Matches a list of Orders against the current order book.
    Rules:
      - Positive qty = buy order. Matches against sell_orders (asks).
      - Negative qty = sell order. Matches against buy_orders (bids).
      - If order price crosses the market (aggressive), fill at MARKET price.
      - If order price is passive (inside the spread), fill only if market moves.
      - Position limits enforced; excess qty silently dropped.
      - Multiple orders for same symbol: processed in list order.
    Returns list of Trade objects and updated positions.
    """

    def __init__(self, position_limits: Dict[str, int]):
        self.position_limits = position_limits

    def match(
        self,
        orders:    Dict[str, List[Order]],
        depths:    Dict[str, OrderDepth],
        positions: Dict[str, int],
        timestamp: int,
    ) -> Tuple[List[Trade], Dict[str, int], float]:
        """Returns (fills, new_positions, realized_cash_delta)."""
        fills      : List[Trade] = []
        new_pos     = dict(positions)
        cash_delta  = 0.0

        for symbol, order_list in orders.items():
            if symbol not in depths:
                continue
            od    = depths[symbol]
            limit = self.position_limits.get(symbol, POSITION_LIMIT)

            for order in order_list:
                qty  = order.quantity
                price = order.price
                pos   = new_pos.get(symbol, 0)

                if qty == 0:
                    continue

                if qty > 0:  # BUY — matches asks
                    # Clip to position limit
                    max_buy = limit - pos
                    qty = min(qty, max_buy)
                    if qty <= 0:
                        continue

                    filled = 0
                    # Sort asks low→high and fill greedily
                    for ask_price in sorted(od.sell_orders.keys()):
                        if filled >= qty:
                            break
                        if ask_price > price:        # passive: don't cross
                            break
                        avail   = -od.sell_orders[ask_price]  # positive vol
                        can_buy = min(avail, qty - filled)
                        if can_buy > 0:
                            fill_price = ask_price  # fill at market (better for us)
                            fills.append(Trade(symbol, fill_price, can_buy,
                                               "SUBMISSION", "", timestamp))
                            cash_delta  -= fill_price * can_buy
                            new_pos[symbol] = new_pos.get(symbol, 0) + can_buy
                            filled      += can_buy

                else:  # SELL — matches bids (qty negative)
                    sell_qty = -qty
                    max_sell  = limit + pos  # how much we can sell short to
                    sell_qty  = min(sell_qty, max_sell)
                    if sell_qty <= 0:
                        continue

                    filled = 0
                    for bid_price in sorted(od.buy_orders.keys(), reverse=True):
                        if filled >= sell_qty:
                            break
                        if bid_price < price:        # passive: don't cross
                            break
                        avail    = od.buy_orders[bid_price]
                        can_sell = min(avail, sell_qty - filled)
                        if can_sell > 0:
                            fill_price = bid_price
                            fills.append(Trade(symbol, fill_price, -can_sell,
                                               "", "SUBMISSION", timestamp))
                            cash_delta  += fill_price * can_sell
                            new_pos[symbol] = new_pos.get(symbol, 0) - can_sell
                            filled       += can_sell

        return fills, new_pos, cash_delta


