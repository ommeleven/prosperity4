def load_algo(algo_path: str):
    """Dynamically load the Trader class from any .py file."""
    spec = importlib.util.spec_from_file_location("trader_module", algo_path)
    mod  = importlib.util.module_from_spec(spec)
    try:
        spec.loader.exec_module(mod)
    except Exception as e:
        raise ImportError(f"Failed to load algo '{algo_path}': {e}") from e
    if not hasattr(mod, "Trader"):
        raise AttributeError(f"'{algo_path}' has no class Trader")
    return mod.Trader


