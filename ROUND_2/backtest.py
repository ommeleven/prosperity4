def color_pnl(v: float) -> str:
    s = f"{v:>+14,.1f}"
    return (GREEN if v >= 0 else RED) + s + RESET


def print_header(title: str):
    width = 72
    print()
    print(BOLD + CYAN + "═" * width + RESET)
    print(BOLD + CYAN + f"  {title}" + RESET)
    print(BOLD + CYAN + "═" * width + RESET)


def print_section(title: str):
    print()
    print(BOLD + f"  ── {title} ──" + RESET)


def print_report(
    algo_path:   str,
    summary:     dict,
    comparison:  Optional[dict],
    investment:  dict,
    log_path:    Optional[str],
    tick_results: List[TickResult],
):
    print_header(f"BACKTEST REPORT  ·  {Path(algo_path).name}")

    # ── PnL Summary ──────────────────────────────────────────────────────────
    print_section("PnL Summary")
    gross = summary["gross_pnl"]
    maf   = summary["algo_maf"]
    net   = summary["net_pnl"]

    print(f"  {'Gross PnL (before MAF):':<35} {color_pnl(gross)}")
    if maf > 0:
        print(f"  {'Market Access Fee (MAF):':<35} {RED}-{maf:>13,.1f}{RESET}")
    print(f"  {'Net PnL:':<35} {color_pnl(net)}")
    target = 200_000
    pct_of_target = net / target * 100
    bar_len = int(min(pct_of_target, 100) / 2)
    bar = "█" * bar_len + "░" * (50 - bar_len)
    color = GREEN if net >= target else (YELLOW if net >= target * 0.5 else RED)
    print(f"  {'Progress to 200k:':<35} {color}[{bar}] {pct_of_target:.1f}%{RESET}")

    # ── Per-Product Stats ─────────────────────────────────────────────────────
    print_section("Per-Product Fill Statistics")
    for sym, stats in summary["product_stats"].items():
        short = sym.replace("_", " ").title()
        print(f"\n  {BOLD}{short}{RESET}")
        print(f"    Buy  trades : {stats['buy_trades']:>6}   total qty: {stats['buy_qty']:>7}   avg price: {stats['avg_buy']:>10.2f}")
        print(f"    Sell trades : {stats['sell_trades']:>6}   total qty: {stats['sell_qty']:>7}   avg price: {stats['avg_sell']:>10.2f}")
        net_pos   = stats['net_qty']
        final_pos = summary['final_positions'].get(sym, 0)
        print(f"    Net position: {net_pos:>+6}   (final: {final_pos:>+4})")

    print(f"\n  Total fills: {summary['total_fills']}")

    # ── PnL Curve (sparkline) ─────────────────────────────────────────────────
    print_section("PnL Curve (sparkline, each char = ~300 ticks)")
    series = [v for _, _, v in summary["pnl_series"]]
    if series:
        n_chars = 60
        step = max(1, len(series) // n_chars)
        sampled = series[::step]
        lo, hi = min(sampled), max(sampled)
        rng = hi - lo if hi != lo else 1
        blocks = " ▁▂▃▄▅▆▇█"
        sparkline = ""
        for v in sampled:
            idx = int((v - lo) / rng * (len(blocks) - 1))
            sparkline += blocks[idx]
        print(f"  {DIM}PnL low: {lo:,.0f}  high: {hi:,.0f}{RESET}")
        print(f"  {CYAN}{sparkline}{RESET}")

    # ── Day Breakdown ─────────────────────────────────────────────────────────
    print_section("Per-Day PnL")
    day_pnl: Dict[int, float] = {}
    day_start: Dict[int, float] = {}
    for r in tick_results:
        if r.day not in day_start:
            day_start[r.day] = r.total_pnl
        day_pnl[r.day] = r.total_pnl

    prev = 0.0
    for d in sorted(day_pnl.keys()):
        end = day_pnl[d]
        delta = end - prev
        print(f"  Day {d:>+2}: end PnL = {color_pnl(end)}   Δ this day = {color_pnl(delta)}")
        prev = end

    # ── Error Summary ─────────────────────────────────────────────────────────
    errors = summary["errors"]
    if errors:
        print_section(f"⚠  Algo Errors ({len(errors)} ticks)")
        for e in errors[:10]:
            print(f"  {RED}{e}{RESET}")
        if len(errors) > 10:
            print(f"  {DIM}... and {len(errors)-10} more{RESET}")

    # ── Log Comparison ────────────────────────────────────────────────────────
    if comparison:
        print_section(f"Log Comparison  [{Path(log_path).name}]")
        if "error" in comparison:
            print(f"  {RED}{comparison['error']}{RESET}")
        else:
            lf = comparison["log_final_pnl"]
            bf = comparison["bt_final_pnl"]
            diff = comparison["pnl_diff"]
            print(f"  {'Submitted log final PnL:':<35} {color_pnl(lf)}")
            print(f"  {'Backtest final PnL:':<35} {color_pnl(bf)}")
            print(f"  {'Difference (bt - log):':<35} {color_pnl(diff)}")
            print(f"  {'Mean |diff| per tick:':<35} {comparison['mean_abs_diff']:>14.2f}")
            print(f"  {'Max  |diff| per tick:':<35} {comparison['max_abs_diff']:>14.2f}")
            print(f"  {'Log submission trades:':<35} {comparison['log_trade_count']:>14}")
            if abs(diff) < 500:
                print(f"  {GREEN}✓ Backtest closely matches submitted log{RESET}")
            elif abs(diff) < 5000:
                print(f"  {YELLOW}⚠ Moderate divergence — possible fill model differences{RESET}")
            else:
                print(f"  {RED}✗ Large divergence — check fill model / order logic{RESET}")

    # ── MAF Analysis ──────────────────────────────────────────────────────────
    print_section("Market Access Fee (MAF) Analysis")
    base_pnl = gross
    with_maf  = base_pnl * 1.25   # +25% volume approx
    maf_uplift = with_maf - base_pnl
    break_even_maf = maf_uplift
    print(f"  {'Estimated gross PnL (no MAF):':<40} {color_pnl(base_pnl)}")
    print(f"  {'Estimated gross PnL (+25% volume):':<40} {color_pnl(with_maf)}")
    print(f"  {'Incremental value of MAF:':<40} {color_pnl(maf_uplift)}")
    print(f"  {'Break-even MAF:':<40} {YELLOW}{break_even_maf:>+14,.1f}{RESET}")
    if maf > 0:
        if maf < break_even_maf:
            print(f"  {GREEN}✓ Your MAF ({maf:,.0f}) is below break-even — profitable if accepted{RESET}")
        else:
            print(f"  {RED}✗ Your MAF ({maf:,.0f}) exceeds break-even — reconsider{RESET}")
    else:
        print(f"  {DIM}No MAF set. Recommend bidding up to {break_even_maf:,.0f}{RESET}")

    # ── Investment Optimiser ──────────────────────────────────────────────────
    print_section("Manual Challenge: Invest & Expand (50,000 budget)")
    inv = investment
    print(f"  {BOLD}Optimal allocation:{RESET}")
    print(f"    Research : {inv['research_pct']:>5.1f}%")
    print(f"    Scale    : {inv['scale_pct']:>5.1f}%")
    print(f"    Speed    : {inv['speed_pct']:>5.1f}%")
    print(f"    Budget used: {inv['budget_used']:>10,.0f}  (speed mult assumed: {inv['speed_mult']:.2f})")
    print(f"    Gross manual PnL: {color_pnl(inv['gross_pnl'])}")
    print(f"    Net   manual PnL: {color_pnl(inv['net_pnl'])}")
    print()
    print(f"  {DIM}Note: Speed multiplier is rank-based; actual result depends on")
    print(f"  competitors' speed investments. Maximise Research × Scale first,{RESET}")
    print(f"  {DIM}then invest remaining budget in Speed to stay competitive.{RESET}")

    print()
    print(BOLD + CYAN + "═" * 72 + RESET)
    print()


