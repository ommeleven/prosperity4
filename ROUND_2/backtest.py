@dataclass
class TickResult:
    day:           int
    timestamp:     int
    position:      Dict[str, int]
    cash:          float
    mark_to_market: float        # unrealized PnL from open positions
    realized_pnl:  float         # cumulative cash PnL
    total_pnl:     float         # realized + MTM
    fills:         List[Trade]
    trader_data:   str
    algo_error:    Optional[str]


def mid_price_from_depth(od: OrderDepth) -> float:
    best_bid = max(od.buy_orders.keys())  if od.buy_orders  else None
    best_ask = min(od.sell_orders.keys()) if od.sell_orders else None
    if best_bid and best_ask:
        return (best_bid + best_ask) / 2
    return best_bid or best_ask or 0.0


def run_backtest(
    TraderClass,
    prices:    pd.DataFrame,
    trades:    pd.DataFrame,
    days:      List[int],
    maf:       float = 0,
    verbose:   bool  = False,
) -> Tuple[List[TickResult], dict]:
    """
    Main backtest loop. Returns tick-by-tick results and a summary dict.
    """
    trader    = TraderClass()
    engine    = MatchingEngine({p: POSITION_LIMIT for p in PRODUCTS})

    positions : Dict[str, int]   = {p: 0 for p in PRODUCTS}
    cash       = 0.0
    trader_data = ""

    # Check if the algo has a bid() method (MAF)
    algo_maf = 0
    if hasattr(trader, "bid") and callable(trader.bid):
        try:
            algo_maf = float(trader.bid())
        except Exception:
            algo_maf = 0

    if maf > 0:
        algo_maf = maf   # command-line override

    # Group price rows by (day, timestamp) for fast lookup
    price_index = prices.groupby(["day", "timestamp"])

    # Build a sorted list of all unique (day, timestamp) ticks
    all_ticks = sorted(prices[["day","timestamp"]].drop_duplicates().itertuples(index=False),
                       key=lambda r: (r.day, r.timestamp))

    # Trade lookup: market trades at each (day, timestamp)
    trade_index = trades.groupby(["day","timestamp"])

    tick_results: List[TickResult] = []
    summary_fills: List[Trade] = []
    own_trades_acc: Dict[str, List[Trade]] = defaultdict(list)

    listings = {p: Listing(p, p, "XIRECS") for p in PRODUCTS}
    observations = Observation()

    for tick in all_ticks:
        day, ts = tick.day, tick.timestamp

        # Build OrderDepth for each product at this tick
        depths: Dict[str, OrderDepth] = {}
        try:
            tick_rows = price_index.get_group((day, ts))
        except KeyError:
            continue

        for _, row in tick_rows.iterrows():
            depths[row["product"]] = build_order_depth(row)

        # Build market_trades for this tick
        market_trades_tick: Dict[str, List[Trade]] = defaultdict(list)
        try:
            tick_trades = trade_index.get_group((day, ts))
            for _, tr in tick_trades.iterrows():
                market_trades_tick[tr["symbol"]].append(
                    Trade(tr["symbol"], tr["price"], tr["quantity"],
                          str(tr.get("buyer", "")), str(tr.get("seller", "")), ts)
                )
        except KeyError:
            pass

        # Build TradingState
        state = TradingState(
            traderData    = trader_data,
            timestamp     = ts,
            listings      = listings,
            order_depths  = {sym: copy.deepcopy(d) for sym, d in depths.items()},
            own_trades    = dict(own_trades_acc),
            market_trades = dict(market_trades_tick),
            position      = dict(positions),
            observations  = observations,
        )

        # Call the algo
        algo_error = None
        result_orders: Dict[str, List[Order]] = {}
        conversions = 0

        try:
            ret = trader.run(state)
            if isinstance(ret, tuple) and len(ret) == 3:
                result_orders, conversions, trader_data = ret
            elif isinstance(ret, tuple) and len(ret) == 2:
                result_orders, conversions = ret
            elif isinstance(ret, dict):
                result_orders = ret
        except Exception as e:
            algo_error = f"t={ts}: {type(e).__name__}: {e}"
            if verbose:
                traceback.print_exc()

        # Clear own_trades for next tick
        own_trades_acc = defaultdict(list)

        # Match orders
        if result_orders:
            fills, positions, delta = engine.match(
                result_orders, depths, positions, ts
            )
            cash += delta
            for f in fills:
                own_trades_acc[f.symbol].append(f)
                summary_fills.append(f)

            if verbose and fills:
                for f in fills:
                    side = "BUY" if f.quantity > 0 else "SELL"
                    print(f"  [{day}|{ts:>7}] {side} {abs(f.quantity):>3}x "
                          f"{f.symbol:<25} @{f.price:.1f}")
        else:
            fills = []

        # Mark-to-market
        mtm = 0.0
        for sym, pos in positions.items():
            od = depths.get(sym, OrderDepth())
            mid = mid_price_from_depth(od)
            mtm += pos * mid

        total_pnl = cash + mtm

        tick_results.append(TickResult(
            day=day, timestamp=ts,
            position=dict(positions),
            cash=cash,
            mark_to_market=mtm,
            realized_pnl=cash,
            total_pnl=total_pnl,
            fills=fills,
            trader_data=trader_data,
            algo_error=algo_error,
        ))

    # Final PnL (mark at last tick mid prices)
    final_pnl = tick_results[-1].total_pnl if tick_results else 0.0
    net_pnl   = final_pnl - algo_maf

    # Per-product fill stats
    product_stats: Dict[str, dict] = {}
    for sym in PRODUCTS:
        sym_fills = [f for f in summary_fills if f.symbol == sym]
        buys  = [f for f in sym_fills if f.quantity > 0]
        sells = [f for f in sym_fills if f.quantity < 0]
        buy_qty   = sum(f.quantity        for f in buys)
        sell_qty  = sum(-f.quantity       for f in sells)
        buy_cost  = sum(f.price*f.quantity for f in buys)
        sell_rev  = sum(f.price*-f.quantity for f in sells)
        avg_buy   = buy_cost  / buy_qty   if buy_qty   else 0
        avg_sell  = sell_rev  / sell_qty  if sell_qty  else 0
        product_stats[sym] = dict(
            buy_trades=len(buys), sell_trades=len(sells),
            buy_qty=buy_qty, sell_qty=sell_qty,
            avg_buy=avg_buy, avg_sell=avg_sell,
            net_qty=buy_qty - sell_qty,
        )

    summary = dict(
        algo_maf=algo_maf,
        gross_pnl=final_pnl,
        net_pnl=net_pnl,
        total_fills=len(summary_fills),
        product_stats=product_stats,
        final_positions=dict(positions),
        errors=[r.algo_error for r in tick_results if r.algo_error],
        pnl_series=[(r.day, r.timestamp, r.total_pnl) for r in tick_results],
    )
    return tick_results, summary


