def load_log(log_path: str) -> dict:
    """Parse a Prosperity .log JSON file."""
    with open(log_path) as f:
        data = json.load(f)

    trades_by_ts: Dict[int, List[dict]] = defaultdict(list)
    for t in data.get("tradeHistory", []):
        trades_by_ts[t["timestamp"]].append(t)

    # Parse activitiesLog into a DataFrame
    raw = data.get("activitiesLog", "")
    acts = pd.read_csv(io.StringIO(raw), sep=";") if raw.strip() else pd.DataFrame()

    return {
        "submission_id": data.get("submissionId", ""),
        "trades_by_ts":  trades_by_ts,
        "activities":    acts,
        "raw":           data,
    }


