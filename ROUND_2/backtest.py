def main():
    parser = argparse.ArgumentParser(
        description="Prosperity Round 2 Backtesting Engine",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=textwrap.dedent("""
        Examples:
          # Basic backtest
          python backtest.py --algo trader.py

          # Backtest + compare against submission log
          python backtest.py --algo trader.py --log submission.log

          # Custom data directory + specific days + MAF override
          python backtest.py --algo trader.py --data-dir ./data --days -1 0 1 --maf 10000

          # Save HTML report
          python backtest.py --algo trader.py --log sub.log --report report.html

          # Verbose (print every fill)
          python backtest.py --algo trader.py --verbose
        """)
    )
    parser.add_argument("--algo",      required=True,  help="Path to your Trader .py file")
    parser.add_argument("--log",       default=None,   help="Path to submission .log JSON file for comparison")
    parser.add_argument("--data-dir",  default=".",    help="Directory containing the 6 CSV data files (default: .)")
    parser.add_argument("--days",      nargs="+", type=int, default=[-1, 0, 1], help="Days to simulate (default: -1 0 1)")
    parser.add_argument("--maf",       type=float, default=0, help="Override MAF value (0 = use algo's bid() if present)")
    parser.add_argument("--verbose",   action="store_true", help="Print every fill to stdout")
    parser.add_argument("--report",    default=None,   help="Save HTML report to this path")
    parser.add_argument("--no-color",  action="store_true", help="Disable terminal colours")
    args = parser.parse_args()

    if args.no_color:
        for name in ["BOLD","GREEN","RED","YELLOW","CYAN","DIM","RESET"]:
            globals()[name] = ""

    # ── Load data ─────────────────────────────────────────────────────────────
    print(f"{CYAN}Loading data from '{args.data_dir}' for days {args.days}…{RESET}")
    try:
        prices, trades = load_data(args.data_dir, args.days)
    except FileNotFoundError as e:
        print(f"{RED}Error: {e}{RESET}")
        sys.exit(1)
    print(f"  Loaded {len(prices):,} price rows, {len(trades):,} trade rows")

    # ── Load algo ─────────────────────────────────────────────────────────────
    print(f"{CYAN}Loading algo: {args.algo}{RESET}")
    try:
        TraderClass = load_algo(args.algo)
    except (ImportError, AttributeError) as e:
        print(f"{RED}Error: {e}{RESET}")
        sys.exit(1)
    print(f"  Trader class loaded ✓")

    # ── Load log (optional) ───────────────────────────────────────────────────
    log_data = None
    if args.log:
        print(f"{CYAN}Loading log: {args.log}{RESET}")
        try:
            log_data = load_log(args.log)
            ntrades = sum(len(v) for v in log_data["trades_by_ts"].values())
            print(f"  Log loaded: {ntrades} submission trades ✓")
        except Exception as e:
            print(f"{YELLOW}Warning: could not load log ({e}){RESET}")

    # ── Run backtest ──────────────────────────────────────────────────────────
    print(f"{CYAN}Running backtest ({len(prices):,} ticks)…{RESET}")
    tick_results, summary = run_backtest(
        TraderClass, prices, trades,
        days=args.days, maf=args.maf, verbose=args.verbose
    )
    print(f"  Done. {summary['total_fills']} fills, gross PnL = {summary['gross_pnl']:+,.1f}")

    # ── Compare with log ──────────────────────────────────────────────────────
    comparison = None
    if log_data:
        comparison = compare_with_log(tick_results, log_data)

    # ── Investment optimiser ──────────────────────────────────────────────────
    investment = optimise_investment()

    # ── Print terminal report ─────────────────────────────────────────────────
    print_report(
        algo_path=args.algo,
        summary=summary,
        comparison=comparison,
        investment=investment,
        log_path=args.log,
        tick_results=tick_results,
    )

    # ── Save HTML ─────────────────────────────────────────────────────────────
    if args.report:
        save_html_report(
            path=args.report,
            algo_path=args.algo,
            summary=summary,
            comparison=comparison,
            investment=investment,
            tick_results=tick_results,
        )

    # Exit code
    sys.exit(0 if summary["net_pnl"] > 0 else 1)


if __name__ == "__main__":
    main()
